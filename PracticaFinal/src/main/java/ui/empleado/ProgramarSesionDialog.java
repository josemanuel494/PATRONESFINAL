    private void cargarPeliculasEnCombo() {
        cmbPelicula.removeAllItems();
        for (Pelicula p : controlador.obtenerPeliculas()) {
            cmbPelicula.addItem(p);
        }
    }
    
    private void cargarSalasEnComboFiltradas() {
        cmbSala.removeAllItems();

        modelo.Pelicula peli = (modelo.Pelicula) cmbPelicula.getSelectedItem();
        if (peli == null) return;

        for (modelo.Sala s : todasLasSalas) {
            // Usa la compatibilidad real del modelo (IMAX solo donde toca)
            if (peli.esCompatibleConSala(s)) {
                cmbSala.addItem(s);
            }
        }

        // Si no hay salas compatibles, avisa
        if (cmbSala.getItemCount() == 0) {
            if (lblAvisoCompatibilidad != null) {
                lblAvisoCompatibilidad.setText("No hay salas compatibles con esta película.");
            }
        } else {
            if (lblAvisoCompatibilidad != null) {
                lblAvisoCompatibilidad.setText(""); // limpia aviso
            }
        }
    }




