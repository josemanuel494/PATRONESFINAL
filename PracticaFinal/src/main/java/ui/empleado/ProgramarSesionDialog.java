    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        String codigoSesion = txtCodigoSesion.getText().trim();
        Pelicula peli = (Pelicula) cmbPelicula.getSelectedItem();
        Sala sala = (Sala) cmbSala.getSelectedItem();

        if (codigoSesion.isEmpty() || peli == null || sala == null) {
            JOptionPane.showMessageDialog(this, "Rellena código, película y sala.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        LocalDate fecha;
        LocalTime hora;
        try {
            fecha = LocalDate.parse(txtFecha.getText().trim(), F_ES);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Fecha inválida. Usa formato DD-MM-AAAA.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            hora = LocalTime.parse(txtHora.getText().trim()); // HH:MM
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Hora inválida. Usa formato HH:MM.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        double precioBase = ((Number) spnPrecioBase.getValue()).doubleValue();

        try {
            controlador.programarSesion(
                    codigoSesion,
                    peli.getCodigo(),
                    fecha,
                    hora,
                    sala.getNumero(),
                    precioBase
            );

            creada = true;
            JOptionPane.showMessageDialog(this, "Sesión programada correctamente.");
            dispose();

        } catch (SalaNoCompatibleIMAXException ex) {
            JOptionPane.showMessageDialog(this, "Esa película no es compatible con la sala seleccionada (IMAX).", "Error", JOptionPane.ERROR_MESSAGE);

        } catch (SalaOcupadaException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Sala ocupada", JOptionPane.ERROR_MESSAGE);

        } catch (EntidadNoEncontradaException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "No encontrado", JOptionPane.ERROR_MESSAGE);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCrearActionPerformed

